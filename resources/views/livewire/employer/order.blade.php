            $(function () {
        $('[data-toggle="tooltip"]').tooltip();
        $(".touchspin-postfix").TouchSpin({
            min: 0,
            max: 100,
            step: 1,
            decimals: 0
        });
    });

    $(document).ready(function () {
        $('.view-package-detail-btn').click(function (event) {
            $('#package-detail-modal').modal('show');
        });

                setTimeout(function(){
            $('.package-standard-qty').val(1).trigger('change');
        },500);
                //update total amount
        function updateTotalAmount() {
            let total_amount = 0;
            $(".amount").each(function (index) {
                let amount = $(this).text();
                amount = parseInt(amount.split(',').join(''));
                total_amount += amount;
            });
            total_amount = total_amount.toString().replace(/\B(?=(\d{3})+(?!\d))/g, ",");
            $('#total-amount, #temporary-amount').html(total_amount + '₫');
        }

        //get amount when clicking up and down
        $('.bootstrap-touchspin-up, .bootstrap-touchspin-down').on('click', function (event) {
            event.preventDefault();
            let qty = $(this).parent().parent().find('.touchspin-postfix').val();
            let package = $(this).parent().parent().find('.touchspin-postfix').attr('package');
            let price = $('#price-' + package).html();
            price = parseInt(price.split(',').join(''));
            let amount = price * qty;
            amount = amount.toString().replace(/\B(?=(\d{3})+(?!\d))/g, ",");
            $('#amount-' + package).html(amount);
            updateTotalAmount();

            //tặng job standard
            if (package == 4) {
                if (qty >= 2) {
                    let html = '<i>+';
                    let countGift = parseInt(qty / 2);
                    html += countGift + ' job tặng</i>';
                    $('#count-job-gift').html(html).show();
                } else {
                    $('#count-job-gift').hide();
                }
            }
        });

        //get price when change input value
        $('.touchspin-postfix').change(function (event) {
            event.preventDefault();
            let qty = $(this).val();
            let package = $(this).attr('package');
            let price = $('#price-' + package).html();
            price = parseInt(price.split(',').join(''));
            let amount = price * qty;
            amount = amount.toString().replace(/\B(?=(\d{3})+(?!\d))/g, ",");
            $('#amount-' + package).html(amount);
            updateTotalAmount();
        });

        //place order and payment
        $('#btn-purchase').on('click', function (event) {
            event.preventDefault();
            $.ajax({
                type: "POST",
                url: '/order/initialize',
                data: $('#order-form').serialize(),
                dataType: 'JSON',
                beforeSend: function () {
                    $('#btn-purchase').button('loading');
                },
                complete: function () {
                    $('#btn-purchase').button('reset');
                },
                success: function (response) {
                    if (response.status) {
                        $(location).attr('href', '/order/confirm');
                    } else {
                        alert(response.msg);
                    }
                }
            });
        });
    });
