            if (utmSource || utmMed || utmCampaign || utmTerm) {
                $('a').attr("href", function() {
                    var currUrl = $(this).attr('href');
                    if (currUrl && !currUrl.includes("#") && !currUrl.includes("void(0)") && !currUrl.includes("tel:") && !currUrl.includes("mailto:")) {
